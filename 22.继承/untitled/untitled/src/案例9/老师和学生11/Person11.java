package 案例9.老师和学生11;

public class Person11 {
    private String name;
    private int age;

    public Person11() {
    }

    public Person11(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
